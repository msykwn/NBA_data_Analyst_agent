"""Analysis agent for NBA statistics."""

from strands import Agent

SYSTEM_PROMPT = """あなたはNBAデータ分析の専門エージェントです。

データ取得エージェントから渡された生データをもとに、以下の方針で分析・回答を生成してください。

## 分析の基本方針
- 回答の基本形式は「数値 + 簡単な傾向コメント」です
- 怪我・出場時間・対戦カードの相性といった深い考察は行わず、数値の傾向を簡潔にコメントしてください

## 質問の種類による分析パターン

### パターン1: 単純な照会(「成績を教えて」など)
- 比較は行わず、渡されたデータを整理して簡潔に要約する
- 主要スタッツ(得点・リバウンド・アシストなど)を提示し、一言の傾向コメントを添える

### パターン2: 明示的な比較・ランキング(「AとBを比較して」「上位5人は?」など)
- ユーザーが指定した対象同士を主要スタッツで比較・ランキング形式で提示する
- どちらが優れているか、差分を簡潔にコメントする

### パターン3: 定型的な傾向質問
- 「調子良い?」→ 直近5試合の平均 vs 今シーズン平均を比較し、上昇・下降・横ばいをコメントする
- 「3Pが得意?」→ 本人の3P成績(3P%・3P試投数) vs 同ポジション平均を比較し、得意/苦手をコメントする
- 「安定してる?」→ 試合ごとのバラつき(最大・最小・標準偏差のコメント)に触れる

## 注意事項
- データが不足している場合は、不足している旨を明示してください
- 数値の比較が必要な場合は、LLMとして最善の計算を行ってください
- 回答は日本語で行ってください
"""

analysis_agent = Agent(
    name="analysis_agent",
    description=(
        "NBAデータの分析専門エージェント。"
        "データ取得エージェントから渡された生データをもとに、"
        "比較・ランキング・要約などの分析を行い、「数値 + 傾向コメント」形式で回答を生成する。"
        "単純な照会・明示的な比較・定型的な傾向質問(「調子良い?」「3Pが得意?」など)に対応する。"
    ),
    system_prompt=SYSTEM_PROMPT,
)


if __name__ == "__main__":
    print("=== 分析エージェント 動作確認 ===\n")

    test_cases = [
        {
            "label": "単純な照会",
            "prompt": (
                "以下のデータをもとに、選手の今シーズン成績を要約してください。\n\n"
                "【データ】\n"
                "選手: レブロン・ジェームズ\n"
                "今シーズン平均: 得点23.5, リバウンド8.3, アシスト9.1, FG%50.2%, 出場試合数68\n"
            ),
        },
        {
            "label": "明示的な比較",
            "prompt": (
                "以下のデータをもとに、2選手の成績を比較してください。\n\n"
                "【データ】\n"
                "選手A: ステフィン・カリー - 得点26.4, リバウンド4.5, アシスト5.1, 3P%41.3%, FG%45.0%\n"
                "選手B: ニコラ・ヨキッチ - 得点26.0, リバウンド12.7, アシスト9.0, 3P%35.8%, FG%58.3%\n"
            ),
        },
        {
            "label": "定型的な傾向質問(調子良い?)",
            "prompt": (
                "以下のデータをもとに、選手の最近の調子を分析してください。\n\n"
                "【データ】\n"
                "選手: ジャ・モラント\n"
                "今シーズン平均: 得点22.1, アシスト8.1, FG%47.3%\n"
                "直近5試合: 得点[31, 28, 19, 27, 33], アシスト[9, 7, 5, 10, 8], FG%[52, 49, 38, 55, 58]\n"
            ),
        },
    ]

    for case in test_cases:
        print(f"【{case['label']}】")
        print(f"プロンプト: {case['prompt'][:60]}...\n")
        result = analysis_agent(case["prompt"])
        print(f"回答: {result}\n")
        print("-" * 60 + "\n")
